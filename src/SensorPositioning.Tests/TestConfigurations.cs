    private static void RunTest(double[][] sensors, double[][] obstacles, Polygon markedArea)
    {
      var objective = new SensorPositionObj(9, 6, 12, 56.3, 0.1555);
      objective.CollisionPenaltyFct = 3;
      objective.OutsideFieldPenaltyFct = 3;
      objective.MarkedAreas.Add(markedArea);
      var obstRandSeed = DateTime.Now.Millisecond;
      objective.Random = new MathNet.Numerics.Random.MersenneTwister(obstRandSeed);
      objective.SetObstacles(obstacles);

      var agents = objective.ToAgents(sensors.SelectMany(x => x).ToArray());
      
      var polygonLists = new List<List<Polygon>>();
      foreach (var agent in agents)
      {
        var imperceptibleSensor = Sensors2D.Imperceptible(
          agent.AreaOfActivity(), 
          objective.OtherObstacles(agents, agent), 
          objective.Field);
        polygonLists.Add(imperceptibleSensor);
      }

      var imperceptible = polygonLists[0];
      for (var i = 1; i < polygonLists.Count; i++)
      {
        imperceptible = Polygon.Intersection(
          imperceptible, polygonLists[i]);
      }

      var seenMarkedArea = Polygon.Area(Polygon.Difference(
                             objective.MarkedAreas, imperceptible)) / 
                           Polygon.Area(objective.MarkedAreas);
      var imperceptibleArea = 1 - Polygon.Area(imperceptible) / objective.Field.Area();
      
      Console.WriteLine("Area: " + imperceptibleArea + 
                        ", MA: " + seenMarkedArea);
    }
    
